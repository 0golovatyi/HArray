#include "stdafx.h"
#include "HArrayVarRAM.h"

bool HArrayVarRAM::delValueByKey(uint32* key,
								 uint32 keyLen)
{
	uint32* pValue = getValueByKey(key, keyLen);

	if (pValue)
	{
		*pValue = 0;

		return true;
	}
	else
	{
		return false;
	}
}
